"""
Multi-Agent System — цепочка специализированных агентов.
Каждый агент имеет свою роль и работает поверх одного LLM.

Агенты:
  Critic   — находит слабые места, риски, недоделки
  Planner  — строит план на основе прошлого опыта
  Executor — предлагает конкретные команды для выполнения
"""

from reasoning_agent import _call_llm, _format_steps


CRITIC_PROMPT = """Ты — Critic, ревьюер рабочих процессов. Тебе даны шаги работы.

Твоя задача — найти проблемы:
1. Какие шаги рискованные и могут сломаться?
2. Где есть пробелы — что забыли сделать?
3. Какие зависимости между шагами неочевидны?
4. Что может пойти не так при повторении?

Будь конкретен. Не хвали — только критика и риски.

Формат:
## Риски
- [риск]: [почему опасно]

## Пробелы
- [что пропущено]

## Рекомендации
- [что сделать чтобы было надёжнее]
"""

PLANNER_PROMPT = """Ты — Planner, архитектор процессов. Тебе даны:
1. Прошлый опыт (шаги из памяти)
2. Новая задача от пользователя

На основе прошлого опыта составь ПЛАН для новой задачи:
- Учти ошибки прошлого (не повторяй их)
- Используй успешные паттерны
- Разбей на конкретные шаги с ожидаемым результатом

Формат:
## План
1. [шаг] → ожидаемый результат
2. [шаг] → ожидаемый результат
...

## Риски (из прошлого опыта)
- [на что обратить внимание]

## Оценка сложности
[low / medium / high] — почему
"""

EXECUTOR_PROMPT = """Ты — Executor, DevOps-инженер. Тебе дан план действий.

Преврати каждый шаг плана в КОНКРЕТНЫЕ команды для выполнения.
Для каждой команды укажи:
- Саму команду (bash/shell)
- Что проверить после выполнения
- Что делать если не сработало (fallback)

Формат:
## Шаг N: [описание]
```bash
команда
```
Проверка: [как убедиться что сработало]
Fallback: [что делать если сломалось]
"""


def run_critic(steps: list[dict]) -> str:
    """Critic анализирует шаги и находит проблемы."""
    formatted = _format_steps(steps)
    return _call_llm(CRITIC_PROMPT, f"Шаги для ревью:\n{formatted}")


def run_planner(steps: list[dict], new_task: str) -> str:
    """Planner строит план на основе опыта + новой задачи."""
    formatted = _format_steps(steps)
    user_msg = f"Новая задача: {new_task}\n\nПрошлый опыт:\n{formatted}"
    return _call_llm(PLANNER_PROMPT, user_msg)


def run_executor(plan: str) -> str:
    """Executor превращает план в конкретные команды."""
    return _call_llm(EXECUTOR_PROMPT, f"План для выполнения:\n{plan}")


def run_full_chain(steps: list[dict], new_task: str) -> dict:
    """Запустить полную цепочку: Planner → Critic → Executor.

    Возвращает результат каждого агента.
    """
    # 1. Planner строит план
    plan = run_planner(steps, new_task)

    # 2. Critic ревьюит текущие шаги + план
    critique = run_critic(steps)

    # 3. Executor делает конкретные команды из плана
    execution = run_executor(plan)

    return {
        "plan": plan,
        "critique": critique,
        "execution": execution,
    }
