/* ---------------------------------------------------------------
 * Import loop
 * -------------------------------------------------------------- */
$created  = 0;
$skipped  = 0;
$errors   = 0;
$total    = count( $reviews );
$rating_sum = 0;

WP_CLI::log( "Importing {$total} reviews..." );

foreach ( $reviews as $i => $review ) {
    $num = $i + 1;
    $rating_sum += $review['rating'];

    // Check for duplicate by author + date
    $existing = get_posts( [
        'post_type'   => 'review',
        'post_status' => 'any',
        'meta_query'  => [
            'relation' => 'AND',
            [
                'key'   => 'review_client_name',
                'value' => $review['author'],
            ],
            [
                'key'   => 'review_date',
                'value' => $review['date'],
            ],
        ],
        'numberposts' => 1,
    ] );

    if ( ! empty( $existing ) ) {
        WP_CLI::log( "[{$num}/{$total}] SKIP (exists): {$review['author']}" );
        $skipped++;
        continue;
    }

    if ( $dry_run ) {
        WP_CLI::log( "[{$num}/{$total}] [dry-run] Would create: {$review['author']} — {$review['kitchen_model']} ({$review['source']}, {$review['rating']}★)" );
        $created++;
        continue;
    }

    // Create post
    $post_id = wp_insert_post( [
        'post_type'   => 'review',
        'post_title'  => $review['author'] . ' — ' . $review['kitchen_model'],
        'post_status' => 'publish',
        'post_date'   => $review['date'] . ' 12:00:00',
        'menu_order'  => $num,
    ], true );

    if ( is_wp_error( $post_id ) ) {
        WP_CLI::warning( "[{$num}/{$total}] ERROR: " . $post_id->get_error_message() );
        $errors++;
        continue;
    }

    // ACF fields
    update_field( 'review_client_name',    $review['author'],        $post_id );
    update_field( 'review_rating',        $review['rating'],        $post_id );
    update_field( 'review_text',          $review['text'],          $post_id );
    update_field( 'review_source',        $review['source'],        $post_id );
    update_field( 'review_source_url',    $review['source_url'],    $post_id );
    update_field( 'review_date',          $review['date'],          $post_id );
    // kitchen_model stored in post excerpt (no dedicated ACF field; review_project links to project CPT)
    wp_update_post( array( 'ID' => $post_id, 'post_excerpt' => $review['kitchen_model'] ) );

    WP_CLI::log( "[{$num}/{$total}] Created (ID {$post_id}): {$review['author']} — {$review['kitchen_model']} ({$review['source']}, {$review['rating']}★)" );
    $created++;
}

/* ---------------------------------------------------------------
 * Summary
 * -------------------------------------------------------------- */
$avg_rating = round( $rating_sum / $total, 1 );

WP_CLI::log( '' );
WP_CLI::log( '── Summary ──' );
WP_CLI::log( "Total:          {$total}" );
WP_CLI::log( "Created:        {$created}" );
WP_CLI::log( "Skipped:        {$skipped}" );
WP_CLI::log( "Errors:         {$errors}" );
WP_CLI::log( "Average rating: {$avg_rating}" );

// Source breakdown
$sources = array_count_values( array_column( $reviews, 'source' ) );
WP_CLI::log( '' );
WP_CLI::log( '── Sources ──' );
foreach ( $sources as $src => $cnt ) {
    WP_CLI::log( "  {$src}: {$cnt}" );
}

if ( $dry_run ) {
    WP_CLI::success( 'Dry-run complete. No data was written.' );
} else {
    WP_CLI::success( 'Reviews import complete.' );
}
