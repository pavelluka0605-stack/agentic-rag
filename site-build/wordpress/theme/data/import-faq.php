WP_CLI::log( "Created: {$created}" );
WP_CLI::log( "Skipped: {$skipped}" );
WP_CLI::log( "Errors:  {$errors}" );

if ( $dry_run ) {
    WP_CLI::success( 'Dry-run complete. No data was written.' );
} else {
    WP_CLI::success( 'FAQ import complete.' );
}
