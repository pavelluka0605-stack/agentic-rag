/* ---------------------------------------------------------------
 * 3. Import loop
 * -------------------------------------------------------------- */
$created  = 0;
$skipped  = 0;
$errors   = 0;
$total    = count( $faqs );

WP_CLI::log( "Importing {$total} FAQ items..." );

foreach ( $faqs as $i => $faq ) {
    $num = $i + 1;

    // Check for duplicate by question text
    $existing = get_posts( [
        'post_type'   => 'faq',
        'post_status' => 'any',
        'meta_key'    => 'faq_question',
        'meta_value'  => $faq['question'],
        'numberposts' => 1,
    ] );

    if ( ! empty( $existing ) ) {
        WP_CLI::log( "[{$num}/{$total}] SKIP (exists): {$faq['question']}" );
        $skipped++;
        continue;
    }

    if ( $dry_run ) {
        WP_CLI::log( "[{$num}/{$total}] [dry-run] Would create: {$faq['question']} [{$faq['category']}]" );
        $created++;
        continue;
    }

    // Create post
    $post_id = wp_insert_post( [
        'post_type'   => 'faq',
        'post_title'  => $faq['question'],
        'post_status' => 'publish',
        'menu_order'  => $num,
    ], true );

    if ( is_wp_error( $post_id ) ) {
        WP_CLI::warning( "[{$num}/{$total}] ERROR: " . $post_id->get_error_message() );
        $errors++;
        continue;
    }

    // ACF fields
    update_field( 'faq_question', $faq['question'], $post_id );
    update_field( 'faq_answer',   $faq['answer'],   $post_id );
    update_field( 'faq_category', $faq['category'],  $post_id );

    // Assign taxonomy term
    wp_set_object_terms( $post_id, $faq['category'], 'faq_category' );

    WP_CLI::log( "[{$num}/{$total}] Created (ID {$post_id}): {$faq['question']}" );
    $created++;
}

/* ---------------------------------------------------------------
 * 4. Summary
